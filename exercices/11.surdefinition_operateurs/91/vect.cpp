#include "vect.h"

/************************************************************************ 
 * Constructor 
 *************************************************************************/ 

vect::vect(int l,int c)
{
   v = new int[size=l*c];
   nb_col = c;
}

/************************************************************************ 
 * Destructor
 *************************************************************************/ 
vect::~vect()
{
   delete v;
}

/************************************************************************ 
 * methods
 *************************************************************************/ 
int& vect::operator () (int nbl, int nbc)
{
   if (nbl < 0 || nbc < 0 || nbl*nbc > size ) nbl = nbc = 0;
   return *(v+(nbl*nb_col + nbc));
}
